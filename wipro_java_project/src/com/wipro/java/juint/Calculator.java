/**
 * Calculator class that provides basic arithmetic operations.
 */
package com.wipro.java.juint;

/**
 * Class containing methods for basic arithmetic operations like addition and subtraction.
 */
public class Calculator {

    /**
     * Adds two integers and returns the result.
     * @param a First integer to add
     * @param b Second integer to add
     * @return The sum of the two integers
     */
    public int add(int a, int b) {
        return a + b; // Returns the sum of a and b
    }
    
    /**
     * Subtracts the second integer from the first and returns the result.
     * @param a The integer from which b will be subtracted
     * @param b The integer to subtract from a
     * @return The difference between a and b
     */
    public int subtract(int a, int b) {
        return a - b; // Returns the difference of a and b
    }

}
